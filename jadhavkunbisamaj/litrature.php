<br />5.	दोन सुळे - 	गणपतीला दोन सुळे असतात. एक पूर्ण व दुसरा अर्धा. पुर्ण सुळा श्रद्धेचा आणि अर्धा सुळा बुद्धीचे प्रतीक आहे. बुद्धी कमी असली तरी चालेल. परंतु स्वतःवर विश्वास आणि परमेश्वरावर दृढ श्रद्धा असली पाहिजे. नेत्याचेही तसेच त्यांच्या सेवाभावी कार्यावर दृढ श्रद्धा असावी. 
<br />6.	चार हात - 	गणपतीला चार हात आहेत. एका हातात अंकुश, दुस-या हातात पाश, तिस-या हातात मोदक आणि चौथ्या हातात आशिर्वाद देत आहे. अंकुश म्हणजे वासना विकारांवर संयम ठेवण्या्चे प्रतीक. पाश हा प्रसंगी इंद्रियांना शिक्षा करण्याचे प्रतीक. मोदक म्हणजे मोद (आनंद) देणारा सात्विक आहार आणि आशिर्वाद हा आपल्या भक्तांचे शुभ चिंतन्याचे, त्यांना अभय देण्याचे प्रतीक. नेतादेखील संयमी, प्रसंगी आपपरभाव न ठेवता कठोर शासन देणारा, सात्विक आहार करणारा आणि आपल्या सुमुहाला अभय देवून त्यांच्याप्रती शुभकामना बाळगणारा असावा. 
