<br />8.	लहान पाय - 	गणपतीचे पाय लहान असल्यामुळे तो लवकर धावू शकत नाही. नेत्यानेही धावत सुटू नये. धीरगंभीर पावले उचलावीत. उतमावीळपणा करु नये. (ससा व कासव यांच्या र्शयतीत शेवटी कासवच जिंकते.)
<br />9.	वाहन उंदीर- 	विष्णूचे वाहन गरुड, शंकरचे नंदी तसेच गणपतीचे वाहन उंदीर आहे. उंदीर हा घराघरात प्रवेशतो. नेत्यालाही आपला संदेश लोकहृदयाच्या घराघरात पोहचविता आला पाहिजे. 
<br />10.	दुर्वा - 	गणपतीला 'दुर्वा' फार आवडतात. दुर्वा रंगहीन, सुगंध नसलेली, जगाच्या  दृष्टीने महत्व नसलेली वनस्पती. नेत्यानेही 'जे का रंजले गांजले' अशा निराधारांना प्रिय मानून आधार दिला पाहिजे. 
<br />11.	लाल फुल - 	गणपतीला 'लाल फुल' प्रिय आहे. लाल रंग हा क्रांतीचे प्रतीक. क्रांती म्हणजे आमुलाग्र परिवर्तन. नेत्यांनीही चांगल्या परिवर्तनाने समाजाच्या उन्नयनाचा ध्यास धरला पाहिजे. 
<br />12.	वक्रतुंड - 	गणपतीचे 'वक्रतुंड' असेही एक नाव आहे. वक्र म्हणजे वाकडे, ज्यांचे तोंड नेहमी वाकडे असते. अर्थात जे सरळ सन्मार्गाने वागत नाहीत. अशा दुर्जनांना दंड देणारा तो 'वक्रतुंड'. नेत्यानेदेखील दुर्जनांना दंड आणि सज्जनांना आधार द्यावा. अशाप्रकारे गणेशोत्सवाच्या शुभमुहुर्तावर गणपतीच्यां अंगाच्या थोडया जरी गुणांचा आजच्या नेत्यांनी स्विकार केला तर ते जनता जनार्दनाला निश्चितच ललामभूत ठरतील.
