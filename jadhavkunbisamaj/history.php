<h4>Jadhav</h4>
  
<p>It is not known how the Jadhav came to be known by that name or when and how they were brought under the generic term Kunbi The home districts of the Jadhav Kunbi are Amaravati, Yavatmal and Nagpur. The community is strictly endogamous; consanguinal marriages with maternal cousins are preferred over those with the paternal However, the number of marriages of such nature are low Marriages are arranged. The preferred minimum age is 22 for males and 18 for females, though these ages are increasing as of 2003 Cremation of the dead is the norm, burial being the exception for children and for those who have died of snake bites.Brahmins are employed for naming and marriage ceremonies. Surnames are varied, their origins unknown. They are generally formed from the place of their dwelling, key events in the family past, or a reference to an animate or inanimate object. Among the rural Jadhavs, the traditional caste council has been replaced by the Akhil Bharatiya Jadhav Kunbi Samaj, a registered regional council located in Nagpur which also engages in social work. The rulings of the statutory gram panchayat are followed at the village level Jadhav males are non-vegetarian but the women generally do not eat meat. There are no further subdivisions amongst the Jadhavs.</p>
