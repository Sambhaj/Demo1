Kunbi (alternatively Kanbi) is a generic term applied to castes of traditionally non-elite tillers in Western India These include the Dhonoje, Ghatole, Hindre, Jadav, Jhare, Khaire, Lewa (Leva Patil), Lonare and Tirole communities of Vidharbha The communities are largely found in the state of Maharashtra but also exist in the states of Madhya Pradesh, Gujarat, Karnataka, Kerala and Goa. Kunbis are included among the Other Backward Classes (OBC) in Maharashtra.[5][a][b] </td></tr>
<tr><td colspan="2">

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sant Tukaram, one of the most revered Varkari saints of the Bhakti tradition of Maharashtra belonged to this community, on the other hand Kumar Munshi Kincaid and Parasanisa, consider him to be of the Vani or grocer caste. Most of the Mawalas serving in the armies of the Maratha Empire under Shivaji came from the community. The Shinde and Gaekwad dynasties of the Maratha Empire are originally of Kunbi origin. In the fourteenth century and later, several Kunbis who had taken up employment as military men in the armies of various rulers underwent a process of Sanskritization and began to identify themselves as Marathas. The boundary between the Marathas and the Kunbi became obscure in the early 20th century due to the effects of colonization, and the two groups came to form one block, the Maratha-Kunbi.
Tensions along caste lines between the Kunbi and the Dalit communities were seen in the Khairlanji killings, and the media have reported sporadic instances of violence against Dalits. Other inter-caste issues include the forgery of caste certificates by politicians, mostly in the grey Kunbi-Maratha caste area, to allow them to run for elections from wards reserved for OBC candidates. In April 2005 the Supreme Court of India ruled that the Marathas are not a sub-caste of Kunbis.
